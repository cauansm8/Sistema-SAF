    private void jButton2ListarReceitasActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ListarReceitasActionPerformed

        ControladorFinanceiro controladorFinanceiro = new ControladorFinanceiro();

        ConnectionFactory.iniciarTabelaTransacao();

        DefaultTableModel modelo = (DefaultTableModel) tabelaReceitas.getModel();

        modelo.setNumRows(0);

        ArrayList<TransacaoFinanceira> receitas = controladorFinanceiro.iniciarListagemDeReceitas();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        for (TransacaoFinanceira r : receitas) {

            Object[] linha = {
                    r.getIdTransacao(),
                    r.getDescricao(),
                    r.getCategoria(),
                    String.format(java.util.Locale.US, "%.2f", r.getValor()),
                    sdf.format(r.getData())
            };

            modelo.addRow(linha);

        }

    }// GEN-LAST:event_jButton2ListarReceitasActionPerformed

    private void jButton3EditarReceitaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton3EditarReceitaActionPerformed

        int linhaSelecionada = tabelaReceitas.getSelectedRow();

        if (linhaSelecionada != -1) {
            try {
                Object valorId = tabelaReceitas.getValueAt(linhaSelecionada, 0);

                int id = Integer.parseInt(tabelaReceitas.getValueAt(linhaSelecionada, 0).toString());
                String descricao = tabelaReceitas.getValueAt(linhaSelecionada, 1).toString();
                String categoria = tabelaReceitas.getValueAt(linhaSelecionada, 2).toString();
                float valor = Float.parseFloat(tabelaReceitas.getValueAt(linhaSelecionada, 3).toString());

                java.awt.Frame framePai = (java.awt.Frame) javax.swing.SwingUtilities.getWindowAncestor(this);

                TelaAlterarReceita telaAlterarReceita = new TelaAlterarReceita(framePai, true, id, descricao, categoria,
                        valor);

                telaAlterarReceita.setVisible(true);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione alguma linha");
        }

    }// GEN-LAST:event_jButton3EditarReceitaActionPerformed

    private void jButton4BuscarReceitaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton4BuscarReceitaActionPerformed
        try {
            int idReceita = -1;
            try {
                idReceita = Integer.parseInt(javax.swing.JOptionPane.showInputDialog(this, "ID da receita:"));
            } catch (NumberFormatException ex) {
            }

            ControladorFinanceiro controladorFinanceiro = new ControladorFinanceiro();

            ConnectionFactory.iniciarTabelaTransacao();

            TransacaoFinanceira r = controladorFinanceiro.iniciarBuscaDeReceitas(idReceita);

            DefaultTableModel modelo = (DefaultTableModel) tabelaReceitas.getModel();

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

            modelo.setNumRows(0);

            Object[] linha = {
                    r.getIdTransacao(),
                    r.getDescricao(),
                    r.getCategoria(),
                    String.format(java.util.Locale.US, "%.2f", r.getValor()),
                    sdf.format(r.getData())
            };

            modelo.addRow(linha);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Receita não encontrada ou Operação cancelada");
        }

    }// GEN-LAST:event_jButton4BuscarReceitaActionPerformed

    private void jButton5ExcluirReceitaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton5ExcluirReceitaActionPerformed

        int linhaSelecionada = tabelaReceitas.getSelectedRow();

        if (linhaSelecionada != -1) {
            try {
                Object valorId = tabelaReceitas.getValueAt(linhaSelecionada, 0);

                int idTransacao = Integer.parseInt(tabelaReceitas.getValueAt(linhaSelecionada, 0).toString());

                float valor = Float.parseFloat(tabelaReceitas.getValueAt(linhaSelecionada, 3).toString());

                int resposta = JOptionPane.showConfirmDialog(
                        null,
                        "O saldo do clube será subtraído por " + valor,
                        "Confirmar Exclusão de Receita",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.WARNING_MESSAGE);

                if (resposta == JOptionPane.YES_OPTION) {
                    ControladorFinanceiro controladorFinanceiro = new ControladorFinanceiro();

                    ConnectionFactory.iniciarTabelaTransacao();

                    int idClube = Sessao.getIdClubeAtual();

                    controladorFinanceiro.iniciarExclusaoDeReceita(idTransacao, idClube);
                    ((DefaultTableModel) tabelaReceitas.getModel()).removeRow(linhaSelecionada);
                } else {
                    return;
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione alguma linha");
        }

    }// GEN-LAST:event_jButton5ExcluirReceitaActionPerformed

