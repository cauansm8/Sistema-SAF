                removeAll();
                setLayout(new BorderLayout(24, 24));
                setBackground(Theme.BACKGROUND);
                setBorder(new EmptyBorder(40, 40, 40, 40));

                // ===== HEADER =====
                JPanel headerPanel = new JPanel();
