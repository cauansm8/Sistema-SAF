                headerPanel.setBackground(Theme.BACKGROUND);

                JLabel lblTitulo = new JLabel("Dashboard");
                lblTitulo.setFont(Theme.FONT_TITLE);
                lblTitulo.setForeground(Theme.CINZA_ESCURO);

