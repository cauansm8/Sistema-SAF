        /**
         * Cria um card estilizado com título e valor colorido.
         */
        private JPanel createCard(String title, JLabel lblValue, Color color) {
                JPanel card = new JPanel(new BorderLayout());
                card.setBackground(Color.WHITE);
                card.setBorder(javax.swing.BorderFactory.createCompoundBorder(
                                javax.swing.BorderFactory.createLineBorder(Theme.CINZA_CLARO_PAINEL, 1),
                                new EmptyBorder(24, 24, 24, 24)));

                JLabel lblTitle = new JLabel(title);
                lblTitle.setFont(Theme.FONT_SUBTITLE);
                lblTitle.setForeground(Theme.CINZA_CLARO);

                lblValue.setFont(new Font(Theme.FONT_FAMILY, Font.BOLD, 28));
                lblValue.setForeground(color);

                card.add(lblTitle, BorderLayout.NORTH);
                card.add(lblValue, BorderLayout.CENTER);

                return card;
        }

        public void atualizarConteudo() {

                ControladorFinanceiro controladorFinanceiro = new ControladorFinanceiro();
                ControladorJogador controladorJogador = new ControladorJogador();
                ControladorPartida controladorPartida = new ControladorPartida();

                ArrayList<TransacaoFinanceira> receitas = controladorFinanceiro.iniciarListagemDeReceitas();
                ArrayList<TransacaoFinanceira> despesas = controladorFinanceiro.iniciarListagemDeDespesas();
                ArrayList<Jogador> jogadores = controladorJogador.iniciarListagemDeJogador();
                ArrayList<Partida> partidas = controladorPartida.iniciarListagemDePartidas();

                float valorTotalReceita = 0;
                float valorTotalDespesa = 0;

                for (TransacaoFinanceira r : receitas) {
                        valorTotalReceita += r.getValor();
                }

                for (TransacaoFinanceira d : despesas) {
                        valorTotalDespesa += d.getValor();
                }

                // Atualiza os labels dos cards
                lblReceitasValor.setText(String.format("R$ %.2f", valorTotalReceita));
                lblDespesasValor.setText(String.format("R$ %.2f", valorTotalDespesa));

                float saldo = valorTotalReceita - valorTotalDespesa;
                lblSaldoValor.setText(String.format("R$ %.2f", saldo));
                lblSaldoValor.setForeground(saldo >= 0 ? Theme.VERDE : Theme.VERMELHO);

                // TROCAR AQUI
                if (jogadores == null) {
                        lblJogadoresValor.setText("0");
                } else {
                        lblJogadoresValor.setText(Integer.toString(jogadores.size()));
                }

                if (partidas == null) {
                        lblPartidasValor.setText("0");
                } else {
                        lblPartidasValor.setText(Integer.toString(partidas.size()));
                }

                receitas.sort(Comparator.comparing(TransacaoFinanceira::getData).reversed());
                despesas.sort(Comparator.comparing(TransacaoFinanceira::getData).reversed());

                TransacaoFinanceira ultimaReceita = !receitas.isEmpty() ? receitas.get(0) : null;
                TransacaoFinanceira ultimaDespesa = !despesas.isEmpty() ? despesas.get(0) : null;

                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

                Object[][] dadosTabela = {
                                {
                                                "Receita",
                                                ultimaReceita != null ? ultimaReceita.getDescricao() : "Nenhuma",
                                                ultimaReceita != null
                                                                ? String.format("R$ %.2f", ultimaReceita.getValor())
                                                                : "R$ 0,00",
                                                ultimaReceita != null ? sdf.format(ultimaReceita.getData()) : "-"
                                },
                                {
                                                "Despesa",
                                                ultimaDespesa != null ? ultimaDespesa.getDescricao() : "Nenhuma",
                                                ultimaDespesa != null
                                                                ? String.format("R$ %.2f", ultimaDespesa.getValor())
                                                                : "R$ 0,00",
                                                ultimaDespesa != null ? sdf.format(ultimaDespesa.getData()) : "-"
                                }
                };

                String[] colunas = { "Tipo", "Descrição", "Valor", "Data" };

                DefaultTableModel modelo = new DefaultTableModel(dadosTabela, colunas);
                jTableDashboard.setModel(modelo);
        }

