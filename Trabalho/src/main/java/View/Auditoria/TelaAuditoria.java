    private void botaoListarAuditoriasActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_botaoListarAuditoriasActionPerformed
        ControladorAuditoria controladorAuditoria = new ControladorAuditoria();

        ConnectionFactory.iniciarTabelaAuditoria();

        DefaultTableModel modelo = (DefaultTableModel) tabelaAuditoria.getModel();

        modelo.setNumRows(0);

        ArrayList<Auditoria> auditorias = controladorAuditoria.listarAuditorias();

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        for (Auditoria a : auditorias) {

            Object[] linha = {
                    a.getIdAuditoria(),
                    a.getTipoDeLog(),
                    a.getNomeUsuario(),
                    a.getEntidadeAfetada(),
                    a.getIdDoRegistroAfetado(),
                    sdf.format(a.getData())
            };

            modelo.addRow(linha);

        }
    }// GEN-LAST:event_botaoListarAuditoriasActionPerformed

