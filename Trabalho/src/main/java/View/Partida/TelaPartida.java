        private void botaoListarActionPerformed(java.awt.event.ActionEvent evt) {
                ControladorPartida controladorPartida = new ControladorPartida();
                ConnectionFactory.iniciarTabelaPartida();
                ArrayList<Partida> partidas = controladorPartida.iniciarListagemDePartidas();
                atualizarTabela(partidas);
        }

        public void atualizarTabela(ArrayList<Partida> partidas) {
                DefaultTableModel modelo = (DefaultTableModel) tabelaPartidas.getModel();
                modelo.setNumRows(0);

                for (Partida partida : partidas) {
                        String dataStr = "";
                        if (partida.getData() != null) {
                                java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
                                dataStr = sdf.format(partida.getData());
                        }

                        Object[] linha = {
                                        partida.getIdPartida(),
                                        partida.getClubeAdversario(),
                                        dataStr,
                                        partida.getLocal(),
                                        partida.getGolsMarcados() + " x " + partida.getGolsSofridos()
                        };

                        modelo.addRow(linha);
                }
        }

        private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {
                int linhaSelecionada = tabelaPartidas.getSelectedRow();

                if (linhaSelecionada != -1) {
                        try {
                                int id = Integer.parseInt(tabelaPartidas.getValueAt(linhaSelecionada, 0).toString());

                                int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir a partida?", "Confirmar Exclusão", javax.swing.JOptionPane.YES_NO_OPTION);
                                if (confirm != javax.swing.JOptionPane.YES_OPTION) {
                                        return;
                                }

                                ControladorPartida controladorPartida = new ControladorPartida();
                                controladorPartida.excluirPartida(id);

                                javax.swing.JOptionPane.showMessageDialog(this, "Partida excluída com sucesso!");
                                ((DefaultTableModel) tabelaPartidas.getModel()).removeRow(linhaSelecionada);
                        } catch (Exception e) {
                                javax.swing.JOptionPane.showMessageDialog(null, "Erro ao excluir: " + e.getMessage());
                        }
                } else {
                        javax.swing.JOptionPane.showMessageDialog(null, "Selecione uma linha para excluir.");
                }
        }

        private void botaoVerDetalhesActionPerformed(java.awt.event.ActionEvent evt) {
                int linhaSelecionada = tabelaPartidas.getSelectedRow();

                if (linhaSelecionada == -1) {
                        javax.swing.JOptionPane.showMessageDialog(this, "Selecione uma partida.");
                        return;
                }

                int idPartida = (int) tabelaPartidas.getValueAt(linhaSelecionada, 0);

                Model.Partida.Partida partida = DataAcessObject.PartidaDAO.buscarPartidaPorId(idPartida);

                if (partida != null) {
                        TelaDetalhesPartida telaDetalhes = new TelaDetalhesPartida(null, true);
                        telaDetalhes.preencherDados(partida);
                        telaDetalhes.setVisible(true);
                }
        }

        private void botaoEditarActionPerformed(java.awt.event.ActionEvent evt) {
                int linhaSelecionada = tabelaPartidas.getSelectedRow();

                if (linhaSelecionada == -1) {
                        javax.swing.JOptionPane.showMessageDialog(this, "Selecione uma partida para editar.");
                        return;
                }

                int idPartida = (int) tabelaPartidas.getValueAt(linhaSelecionada, 0);

                Model.Partida.Partida partida = DataAcessObject.PartidaDAO.buscarPartidaPorId(idPartida);

                if (partida != null) {
                        TelaEditarPartida telaEditar = new TelaEditarPartida(null, true, partida);
                        telaEditar.setVisible(true);

                        // Recarregar a tabela após editar
                        botaoListarActionPerformed(null);
                }
        }

