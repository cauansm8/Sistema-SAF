    private void customizarLayout() {
        getContentPane().removeAll();
        getContentPane().setLayout(new java.awt.BorderLayout());
        
        // --- WEST: Sidebar ---
        javax.swing.JPanel sidebar = new javax.swing.JPanel();
        sidebar.setLayout(new javax.swing.BoxLayout(sidebar, javax.swing.BoxLayout.Y_AXIS));
        sidebar.setBackground(View.Theme.SIDEBAR);
        sidebar.setPreferredSize(new java.awt.Dimension(220, 0));
        sidebar.setBorder(new javax.swing.border.EmptyBorder(24, 12, 24, 12));
        
        // Logo / Titulo
        javax.swing.JLabel lblLogo = new javax.swing.JLabel("Sistema SAF");
        lblLogo.setFont(View.Theme.FONT_TITLE);
        lblLogo.setForeground(View.Theme.BRANCO);
        lblLogo.setAlignmentX(java.awt.Component.CENTER_ALIGNMENT);
        
        javax.swing.JLabel lblRole = new javax.swing.JLabel("");
        if (Model.Sessao.getUsuarioLogado() != null) {
            int nivel = Model.Sessao.getUsuarioLogado().getNivel_usuario();
            String nome = Model.Sessao.getUsuarioLogado().getNome_usuario();
            if(nivel == 0) {
                lblRole.setText(nome + " | " + "Administrador");
            }
            else if(nivel == 1) {
                lblRole.setText(nome + " | " + "Gestor Financeiro");
            }
            else if(nivel == 2) {
                lblRole.setText(nome + " | " + "Operador");
            }
        }
        
        lblRole.setFont(View.Theme.FONT_SUBTITLE);
        lblRole.setForeground(View.Theme.CINZA_CLARO_CLARO);
        lblRole.setAlignmentX(java.awt.Component.CENTER_ALIGNMENT);
        
        sidebar.add(lblLogo);
        sidebar.add(javax.swing.Box.createVerticalStrut(4));
        sidebar.add(lblRole);
        sidebar.add(javax.swing.Box.createVerticalStrut(32));
        
        // Botoes menu
        menus = new javax.swing.JButton[] {
            botaoDashboard, botaoUsuarios, botaoJogadores, botaoPartida,
            botaoDespesa1, botaoReceitas, botaoClubes, botaoRelatorio, botaoRelatorioDesempenho, botaoAuditoria
        };
        
        for (javax.swing.JButton btn : menus) {
            btn.setMaximumSize(new java.awt.Dimension(200, 36));
            btn.setAlignmentX(java.awt.Component.CENTER_ALIGNMENT);
            btn.setBackground(View.Theme.SIDEBAR);
            btn.setForeground(View.Theme.CINZA_CLARO_CLARO);
            btn.setFont(View.Theme.FONT_MENU);
            btn.setBorderPainted(false);
            btn.setFocusPainted(false);
            btn.setContentAreaFilled(false);
            btn.setOpaque(true);
            btn.setHorizontalAlignment(javax.swing.SwingConstants.LEFT);
            btn.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));

            // Hover: muda cor ao entrar, restaura ao sair (exceto se for o botão ativo)
            btn.addMouseListener(new java.awt.event.MouseAdapter() {
                @Override
                public void mouseEntered(java.awt.event.MouseEvent evt) {
                    if (btn != botaoAtivo && btn.isEnabled()) {
                        btn.setBackground(View.Theme.SIDEBAR_HOVER);
                        btn.setForeground(View.Theme.BRANCO);
                    }
                }
                @Override
                public void mouseExited(java.awt.event.MouseEvent evt) {
                    if (btn != botaoAtivo && btn.isEnabled()) {
                        btn.setBackground(View.Theme.SIDEBAR);
                        btn.setForeground(View.Theme.CINZA_CLARO_CLARO);
                    }
                }
            });

            sidebar.add(btn);
            sidebar.add(javax.swing.Box.createVerticalStrut(8));
        }
        
        sidebar.add(javax.swing.Box.createVerticalGlue());
        
        // Botão sair
        botaoSair.setMaximumSize(new java.awt.Dimension(200, 36));
        botaoSair.setAlignmentX(java.awt.Component.CENTER_ALIGNMENT);
        View.Theme.styleButtonDanger(botaoSair);
        sidebar.add(botaoSair);
        
        // --- CENTER: Conteúdo principal ---
        painelConteudo.setBackground(View.Theme.BACKGROUND);
        
        getContentPane().add(sidebar, java.awt.BorderLayout.WEST);
        getContentPane().add(painelConteudo, java.awt.BorderLayout.CENTER);
        
        getContentPane().setBackground(View.Theme.BACKGROUND);
        
        // Carrega tela de boas vindas inicial
        mostrarTela(new TelaBoasVindas());
    }

    private void configurarAcessos() {
        if (Model.Sessao.getUsuarioLogado() == null) {
            return;
        }

        int nivel = Model.Sessao.getUsuarioLogado().getNivel_usuario();

        // 0 = Administrador (tem acesso a tudo)
        // 1 = Gestor Financeiro
        // 2 = Operador

        if (nivel == 1) { // Gestor Financeiro
            botaoClubes.setEnabled(false);
            botaoPartida.setEnabled(false);
            botaoRelatorioDesempenho.setEnabled(false);
            botaoAuditoria.setEnabled(false);
        } else if (nivel == 2) { // Operador
            botaoClubes.setEnabled(false);
            botaoJogadores.setEnabled(false);
            botaoDespesa1.setEnabled(false);
            botaoReceitas.setEnabled(false);
            botaoRelatorio.setEnabled(false);
            botaoAuditoria.setEnabled(false);
        }
    }
