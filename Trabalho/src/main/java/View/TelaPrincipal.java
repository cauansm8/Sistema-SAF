    private void mostrarTela(javax.swing.JPanel novaTela) {
        painelConteudo.removeAll();

        // Força o painel a usar BorderLayout para expandir o conteúdo 100%
        painelConteudo.setLayout(new java.awt.BorderLayout());
        painelConteudo.add(novaTela, java.awt.BorderLayout.CENTER);

        painelConteudo.revalidate();
        painelConteudo.repaint();
    }

    /**
     * Marca o botão como ativo (cor de hover fixa) e desmarca o anterior.
     */
    private void marcarBotaoAtivo(javax.swing.JButton btn) {
        // Desmarca o botão anterior
        if (botaoAtivo != null) {
            botaoAtivo.setBackground(View.Theme.SIDEBAR);
            botaoAtivo.setForeground(View.Theme.CINZA_CLARO_CLARO);
        }
        // Marca o novo botão
        botaoAtivo = btn;
        botaoAtivo.setBackground(View.Theme.SIDEBAR_HOVER);
        botaoAtivo.setForeground(View.Theme.BRANCO);
    }

