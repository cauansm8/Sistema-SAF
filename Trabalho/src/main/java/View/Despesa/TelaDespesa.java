    private void jButton2ListarDespesasActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ListarDespesasActionPerformed

        ControladorFinanceiro controladorFinanceiro = new ControladorFinanceiro();

        ConnectionFactory.iniciarTabelaTransacao();

        DefaultTableModel modelo = (DefaultTableModel) tabelaDespesas.getModel();

        modelo.setNumRows(0);

        ArrayList<TransacaoFinanceira> despesas = controladorFinanceiro.iniciarListagemDeDespesas();

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        for (TransacaoFinanceira d : despesas) {

            Object[] linha = {
                    d.getIdTransacao(),
                    d.getDescricao(),
                    d.getCategoria(),
                    String.format(java.util.Locale.US, "%.2f", d.getValor()),
                    sdf.format(d.getData())
            };

            modelo.addRow(linha);

        }

    }// GEN-LAST:event_jButton2ListarDespesasActionPerformed

    private void jButton3EditarDespesaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton3EditarDespesaActionPerformed

        int linhaSelecionada = tabelaDespesas.getSelectedRow();

        if (linhaSelecionada != -1) {
            try {
                Object valorId = tabelaDespesas.getValueAt(linhaSelecionada, 0);

                int id = Integer.parseInt(tabelaDespesas.getValueAt(linhaSelecionada, 0).toString());
                String descricao = tabelaDespesas.getValueAt(linhaSelecionada, 1).toString();
                String categoria = tabelaDespesas.getValueAt(linhaSelecionada, 2).toString();
                float valor = Float.parseFloat(tabelaDespesas.getValueAt(linhaSelecionada, 3).toString());

                java.awt.Frame framePai = (java.awt.Frame) javax.swing.SwingUtilities.getWindowAncestor(this);

                TelaAlterarDespesa telaAlterarDespesa = new TelaAlterarDespesa(framePai, true, id, descricao, categoria,
                        valor);

                telaAlterarDespesa.setVisible(true);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione alguma linha");
        }
    }// GEN-LAST:event_jButton3EditarDespesaActionPerformed

    private void jButton4BuscarDespesaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton4BuscarDespesaActionPerformed
        try {
            int idReceita = -1;
            try {
                idReceita = Integer.parseInt(javax.swing.JOptionPane.showInputDialog(this, "ID da despesa:"));
            } catch (NumberFormatException ex) {
            }

            ControladorFinanceiro controladorFinanceiro = new ControladorFinanceiro();

            ConnectionFactory.iniciarTabelaTransacao();

            TransacaoFinanceira r = controladorFinanceiro.iniciarBuscaDeDespesas(idReceita);

            DefaultTableModel modelo = (DefaultTableModel) tabelaDespesas.getModel();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            modelo.setNumRows(0);

            Object[] linha = {
                    r.getIdTransacao(),
                    r.getDescricao(),
                    r.getCategoria(),
                    String.format(java.util.Locale.US, "%.2f", r.getValor()),
                    sdf.format(r.getData())
            };

            modelo.addRow(linha);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Despesa não encontrada ou Operação cancelada");
        }

    }// GEN-LAST:event_jButton4BuscarDespesaActionPerformed

    private void jButton5ExcluirDespesaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton5ExcluirDespesaActionPerformed

        int linhaSelecionada = tabelaDespesas.getSelectedRow();

        if (linhaSelecionada != -1) {
            try {
                Object valorId = tabelaDespesas.getValueAt(linhaSelecionada, 0);

                int idTransacao = Integer.parseInt(tabelaDespesas.getValueAt(linhaSelecionada, 0).toString());

                float valor = Float.parseFloat(tabelaDespesas.getValueAt(linhaSelecionada, 3).toString());

                int resposta = JOptionPane.showConfirmDialog(
                        null,
                        "O saldo do clube será somado por " + valor,
                        "Confirmar Exclusão de Despesa",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.WARNING_MESSAGE);

                if (resposta == JOptionPane.YES_OPTION) {
                    ControladorFinanceiro controladorFinanceiro = new ControladorFinanceiro();

                    ConnectionFactory.iniciarTabelaTransacao();

                    int idClube = Sessao.getIdClubeAtual();

                    controladorFinanceiro.iniciarExclusaoDeDespesa(idTransacao, idClube);
                    ((DefaultTableModel) tabelaDespesas.getModel()).removeRow(linhaSelecionada);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione alguma linha");
        }

    }// GEN-LAST:event_jButton5ExcluirDespesaActionPerformed

